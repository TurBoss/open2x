/*
	File: mmsp2.h

	This file defines the MMSP2 special function registers, in the 920T core's memory space.

	The registers are each defined in 3 forms -

	- As a constant number, the address of the register. These are prefixed with "nMSP_", e.g.

> nMSP_SPECIAL_FUNCTION_REGISTER = (0xC0001234)

	- As a pointer to the register. These are prefixed with "pMSP_". The type of the register is defined
   	by its size, ie. "unsigned char" for 1 byte, "unsigned short" for 2 bytes, and "unsigned int" for 4 bytes, e.g.

> pMSP_SPECIAL_FUNCTION_REGISTER = ((volatile unsigned short *)nMSP_SPECIAL_FUNCTION_REGISTER)

	- As a memory access which can be used as an l-value or an r-value. These are prefixed with "MSP_", e.g.

> MSP_SPECIAL_FUNCTION_REGISTER = (*pMSP_SPECIAL_FUNCTION_REGISTER)

	Other files:

	This file is duplicated, with different addresses, for use with code for the 940T core-
	- *mmsp2_940_1.h* - use this when the 940T core is running with 940BANK set to 0x01.
	- *mmsp2_940_2.h* - use this when the 940T core is running with 940BANK set to 0x02.
	- *mmsp2_940_3.h* - use this when the 940T core is running with 940BANK set to 0x03.
*/

